public class GuildRanking {
    private String name;
    private Double game;
    private String day;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public Double getGame() {
        return game;
    }

    public void setGame(Double game) {
        this.game = game;
    }

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }
}
