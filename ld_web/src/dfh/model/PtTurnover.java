package dfh.model;

public class PtTurnover {
	
	private Double betCredit;
	
	private Double amount;
	
	private Double payOut;
	
	private String ptInfoStr;
	
	public PtTurnover(){
		
	}

	public Double getBetCredit() {
		return betCredit;
	}

	public void setBetCredit(Double betCredit) {
		this.betCredit = betCredit;
	}

	public Double getAmount() {
		return amount;
	}

	public void setAmount(Double amount) {
		this.amount = amount;
	}

	public Double getPayOut() {
		return payOut;
	}

	public void setPayOut(Double payOut) {
		this.payOut = payOut;
	}

	public String getPtInfoStr() {
		return ptInfoStr;
	}

	public void setPtInfoStr(String ptInfoStr) {
		this.ptInfoStr = ptInfoStr;
	}
	
	

}
