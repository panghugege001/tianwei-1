package app.model.vo;

public class CommissionListVO {

    private String yearmonth;
    private String loginname;
    private String ximaAmount;
    private String firstDepositAmount;
    private String agAmount;
    private String otherAmount;
    private String remark;
	public String getYearmonth() {
		return yearmonth;
	}
	public void setYearmonth(String yearmonth) {
		this.yearmonth = yearmonth;
	}
	public String getLoginname() {
		return loginname;
	}
	public void setLoginname(String loginname) {
		this.loginname = loginname;
	}
	public String getXimaAmount() {
		return ximaAmount;
	}
	public void setXimaAmount(String ximaAmount) {
		this.ximaAmount = ximaAmount;
	}
	public String getFirstDepositAmount() {
		return firstDepositAmount;
	}
	public void setFirstDepositAmount(String firstDepositAmount) {
		this.firstDepositAmount = firstDepositAmount;
	}
	public String getAgAmount() {
		return agAmount;
	}
	public void setAgAmount(String agAmount) {
		this.agAmount = agAmount;
	}
	public String getOtherAmount() {
		return otherAmount;
	}
	public void setOtherAmount(String otherAmount) {
		this.otherAmount = otherAmount;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}

}